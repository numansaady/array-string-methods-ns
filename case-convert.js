const userName = "Sabiha";
const savedUserName = "sabiha";

if (userName == savedUserName) {
  console.log("User is exists");
}

// String Converted to Lower Case
userName.toLowerCase;
savedUserName.toLowerCase;

if (userName.toLowerCase() == savedUserName.toLowerCase()) {
  console.log("User is exists");
}

// String Converted to Upper Case
userName.toUpperCase;
savedUserName.toUpperCase;

if (userName.toUpperCase() == savedUserName.toUpperCase()) {
  console.log("User is exists");
}
